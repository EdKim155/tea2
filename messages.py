async def handle_text_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç —Ç–µ–∫—Å—Ç–æ–≤—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è"""
    text = update.message.text
    user_id = update.effective_user.id
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—é –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    if not is_user_registered(user_id):
        from handlers.commands import start_command
        return await start_command(update, context)
    
    # –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏–π –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Ç–µ–∫—Å—Ç–∞
    if text == "üõçÔ∏è –ö–∞—Ç–∞–ª–æ–≥":
        from handlers.commands import catalog_command
        return await catalog_command(update, context)
    
    elif text == "üõí –ö–æ—Ä–∑–∏–Ω–∞":
        from handlers.callbacks import show_cart
        return await show_cart(update, context)
    
    elif text == "üì± –ú–æ–π –ø—Ä–æ—Ñ–∏–ª—å":
        from handlers.registration import show_profile
        return await show_profile(update, context)
    
    elif text == "üìã –ú–æ–∏ –∑–∞–∫–∞–∑—ã":
        from handlers.callbacks import show_orders
        return await show_orders(update, context)
    
    elif text == "‚ÑπÔ∏è –û –º–∞–≥–∞–∑–∏–Ω–µ":
        from handlers.commands import about_command
        return await about_command(update, context)
    
    elif text == "‚òéÔ∏è –°–≤—è–∑–∞—Ç—å—Å—è —Å –Ω–∞–º–∏":
        from handlers.commands import contact_command
        return await contact_command(update, context)
    
    else:
        # –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞—Ö–æ–¥–∏—Ç—Å—è –≤ –≥–ª–∞–≤–Ω–æ–º –º–µ–Ω—é
        return await handle_unknown_message(update, context)

async def handle_unknown_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è"""
    await update.message.reply_text(
        "–ò–∑–≤–∏–Ω–∏—Ç–µ, —è –Ω–µ –ø–æ–Ω–∏–º–∞—é —ç—Ç—É –∫–æ–º–∞–Ω–¥—É. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ –º–µ–Ω—é –∏–ª–∏ /help –¥–ª—è —Å–ø–∏—Å–∫–∞ –∫–æ–º–∞–Ω–¥.",
        reply_markup=get_main_menu_keyboard()
    )
    return MAIN_MENU

async def handle_address_input(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –≤–≤–æ–¥ –∞–¥—Ä–µ—Å–∞ –¥–æ—Å—Ç–∞–≤–∫–∏"""
    user_id = update.effective_user.id
    address = update.message.text
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –∞–¥—Ä–µ—Å –¥–æ—Å—Ç–∞–≤–∫–∏
    user_context = get_user_context(user_id)
    user_context.set_checkout_data('shipping_address', address)
    
    # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –¥–ª—è –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è –∑–∞–∫–∞–∑–∞
    checkout_data = user_context.get_checkout_data()
    payment_method = checkout_data.get('payment_method', '–ù–µ –≤—ã–±—Ä–∞–Ω')
    
    # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º —Å–ø–æ—Å–æ–± –æ–ø–ª–∞—Ç—ã
    if payment_method == 'card':
        payment_text = 'üí≥ –û–ø–ª–∞—Ç–∞ –∫–∞—Ä—Ç–æ–π –æ–Ω–ª–∞–π–Ω'
    elif payment_method == 'cash':
        payment_text = 'üíµ –û–ø–ª–∞—Ç–∞ –Ω–∞–ª–∏—á–Ω—ã–º–∏ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏'
    else:
        payment_text = '–°–ø–æ—Å–æ–± –æ–ø–ª–∞—Ç—ã –Ω–µ –≤—ã–±—Ä–∞–Ω'
    
    confirmation_text = f"""
<b>–ü–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ –∑–∞–∫–∞–∑–∞</b>

<b>–ê–¥—Ä–µ—Å –¥–æ—Å—Ç–∞–≤–∫–∏:</b>
{address}

<b>–°–ø–æ—Å–æ–± –æ–ø–ª–∞—Ç—ã:</b>
{payment_text}

–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ –¥–∞–Ω–Ω—ã–µ –∏ –ø–æ–¥—Ç–≤–µ—Ä–¥–∏—Ç–µ –∑–∞–∫–∞–∑.
"""
    await update.message.reply_html(
        confirmation_text,
        reply_markup=get_confirm_order_keyboard()
    )
    
    return CONTACT_US

